# 튜플 연산하기 +, *
# 튜플은 수정 및 삭제는 불가능하지만 튜플을 추가하거나 곱연산은 가능함
# 튜플은 리스트와 같이 인덱싱과 슬라이싱을 지원함
t1 = (1, 2, "a", "b")
print("튜플 인덱싱하기 : {0}".format(t1[-1]))
print("튜플 슬라이싱하기 : {0}".format(t1[:3]))

print()
t2 = (3, 4)
print("튜플 + 연산하기 : {0}".format(t1 + t2))
print("튜플 * 연산하기 : {0}".format(t1 * 3))
